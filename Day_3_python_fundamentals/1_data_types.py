#data types

#integers
a = 10
print(a)
print(type(a))

#floats
b = 10.5
print(b)
print(type(b))

#strings
c = "Hello, World!"
print(c)
print(type(c))

#booleans
d = True
print(d)
print(type(d))

#lists
e = [1, 2, 3, 4, 5]
print(e)
print(type(e))

#tuples
f = (1, 2, 3, 4, 5)
print(f)
print(type(f))

#dictionaries
g = {"name": "John", "age": 30}
print(g)
print(type(g)) 

#sets
h = {1, 2, 3, 4, 5}
print(h)
print(type(h))

#complex
i = 10 + 5j
print(i)
print(type(i))

#none
j = None
print(j)
print(type(j))